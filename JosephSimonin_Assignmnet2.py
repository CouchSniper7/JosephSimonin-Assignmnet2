# Average weight of 4 Lobsters
print("Welcome to Assignment 2: Average weight of 4 lobsters in pounds.")

#Get weights of lobsters
lobster1 = float(input("Please enter the weight of the 1st lobster in lbs: "))
lobster2 = float(input("Please enter the weight of the 2nd lobster in lbs: "))
lobster3 = float(input("Please enter the weight of the 3rd lobster in lbs: "))
lobster4 = float(input("Please enter the weight of the 4th lobster in lbs: "))

#Calculate Average of lobsters
Average = (lobster1 + lobster2 + lobster3 + lobster4) / 4

#Output Average
print(f"The average weight of the 4 entered lobster weights are {Average}lbs")
